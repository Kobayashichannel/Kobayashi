
public class ArithmeticExercise {

    //编写一个main方法
    public static void main(String[] args) {
        
        int a = 1 >> 2; //1向右移2位
        int b = -1 >> 2;
        int c = 1 << 2;
        int d = -1 << 2;
        int e = 3 >>> 2;
        //a,b,c,d,e结果是多少
        System.out.println("a=" + a);
        System.out.println("b=" + b);
        System.out.println("c=" + c);
        System.out.println("d=" + d);
        System.out.println("e=" + e);
        
    }
}